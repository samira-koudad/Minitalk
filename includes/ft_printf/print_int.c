#include "ft_printf.h"

int	print_int(int n)
{
	int	count;

	count = 0;
	if (n == -2147483648)
		return (write(1, "-2147483648", 11));
	if (n < 0)
	{
		count += write(1, "-", 1);
		n = -n;
	}
	if (n >= 10)
		count += print_int(n / 10);
	count += print_char((n % 10) + '0');
	return (count);
}
